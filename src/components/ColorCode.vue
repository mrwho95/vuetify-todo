<template>
  <div>
        <input 
        v-model="colorCode"
        type="text" 
        placeholder="Enter color code"
        >
    </div>
</template>

<script>
export default {
  computed: {
        colorCode: {
            get() {
                return this.$store.state.colorCode
            },
            set(newValue) {
                // this.$store.commit('setColorCode', newValue);
                this.$store.dispatch('setColorCode', newValue);
            }
        }
    },
}
</script>

